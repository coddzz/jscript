
// .split() .join() .reverse()

let para = "Welcome to JavaScript";
let array = ["Welcome","to","JavaScript"]

console.log(para.split(" "));       // [ 'Welcome', 'to', 'JavaScript' ]
console.log(array.join(" "));       // Welcome to JavaScript
console.log(array.reverse());       // [ 'JavaScript', 'to', 'Welcome' ]
